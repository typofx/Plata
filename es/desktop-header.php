<script>
function setCookie(name, value, days) {
    const date = new Date();
    date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
    const expires = "expires=" + date.toUTCString();
    const existingCookie = getCookie(name);

    // Verifica se o cookie já existe
    if (existingCookie !== "") {
        // Se o cookie existe, atualiza o valor do cookie existente
        document.cookie = name + "=" + value + "; " + expires + "; path=/";
    } else {
        // Se o cookie não existe, cria um novo cookie
        document.cookie = name + "=" + value + "; " + expires + "; path=/";
    }
}


function getCookie(name) {
    const cname = name + "=";
    const decodedCookie = decodeURIComponent(document.cookie);
    const cookieArray = decodedCookie.split(';');
    for (let i = 0; i < cookieArray.length; i++) {
        let c = cookieArray[i];
        while (c.charAt(0) === ' ') {
            c = c.substring(1);
        }
        if (c.indexOf(cname) === 0) {
            return c.substring(cname.length, c.length);
        }
    }
    return "";
}

function applyTheme(theme) {
    if (theme === "dark") {
        console.log("Applying Dark Mode");
        document.body.classList.add("dark-mode");
        document.querySelector("header").classList.add("dark-mode");
    } else {
        console.log("Applying Light Mode");
        document.body.classList.remove("dark-mode");
        document.querySelector("header").classList.remove("dark-mode");
    }
}

// Verifica o cookie quando a página carrega
window.addEventListener('load', () => {
    const savedTheme = getCookie("theme");
    applyTheme(savedTheme);
});

</script>

